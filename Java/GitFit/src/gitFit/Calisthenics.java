package gitFit;

public class Calisthenics {
	private int calisthenicsId;
	private int pushups;
	private int situps;
	private int pullups;
	private int squats;
	private int dips;
	private int programId;
	
	public Calisthenics() {
		
	}

	public int getCalisthenicsId() {
		return calisthenicsId;
	}

	public void setCalisthenicsId(int calisthenicsId) {
		this.calisthenicsId = calisthenicsId;
	}

	public int getPushups() {
		return pushups;
	}

	public void setPushups(int pushups) {
		this.pushups = pushups;
	}

	public int getSitups() {
		return situps;
	}

	public void setSitups(int situps) {
		this.situps = situps;
	}

	public int getPullups() {
		return pullups;
	}

	public void setPullups(int pullups) {
		this.pullups = pullups;
	}

	public int getSquats() {
		return squats;
	}

	public void setSquats(int squats) {
		this.squats = squats;
	}

	public int getDips() {
		return dips;
	}

	public void setDips(int dips) {
		this.dips = dips;
	}

	public int getProgramId() {
		return programId;
	}

	public void setProgramId(int programId) {
		this.programId = programId;
	}
	

}
