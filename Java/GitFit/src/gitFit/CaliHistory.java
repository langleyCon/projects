package gitFit;

public class CaliHistory {
	
	private int id;
	private int push;
	private int pull;
	private int sit;
	private int squat;
	private int dip;
	private String date;
	private String username;
	
	public CaliHistory() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getPush() {
		return push;
	}

	public void setPush(int push) {
		this.push = push;
	}

	public int getPull() {
		return pull;
	}

	public void setPull(int pull) {
		this.pull = pull;
	}

	public int getSit() {
		return sit;
	}

	public void setSit(int sit) {
		this.sit = sit;
	}

	public int getSquat() {
		return squat;
	}

	public void setSquat(int squat) {
		this.squat = squat;
	}

	public int getDip() {
		return dip;
	}

	public void setDip(int dip) {
		this.dip = dip;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

}
