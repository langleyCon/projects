package gitFit;

public class CaliGoal {

	private int id;
	private int type;
	private int pushGoal;
	private int sitGoal;
	private int pullGoal;
	private int squatGoal;
	private int dipGoal;
	private String name;
	private String username;
	
	public CaliGoal() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getPushGoal() {
		return pushGoal;
	}

	public void setPushGoal(int pushGoal) {
		this.pushGoal = pushGoal;
	}

	public int getSitGoal() {
		return sitGoal;
	}

	public void setSitGoal(int sitGoal) {
		this.sitGoal = sitGoal;
	}

	public int getPullGoal() {
		return pullGoal;
	}

	public void setPullGoal(int pullGoal) {
		this.pullGoal = pullGoal;
	}

	public int getSquatGoal() {
		return squatGoal;
	}

	public void setSquatGoal(int squatGoal) {
		this.squatGoal = squatGoal;
	}

	public int getDipGoal() {
		return dipGoal;
	}

	public void setDipGoal(int dipGoal) {
		this.dipGoal = dipGoal;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}
	
}
